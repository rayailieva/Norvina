package notino.domain.models.view;

public class BrandViewModel {

    private String id;
    private String name;

    public BrandViewModel(){}

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
