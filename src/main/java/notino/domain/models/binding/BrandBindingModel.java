package notino.domain.models.binding;

public class BrandBindingModel {

    private String id;
    private String name;

    public BrandBindingModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
